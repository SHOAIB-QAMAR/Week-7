# 1. Change Directory — cd

# The cd (change directory) command is used to move between directories.
#       cd [directory_path]
# 
#  i. Go to a specific directory:
#         cd /home/user/Documents
#  ii. Go to the home directory:
#         cd
#  iii. Go up one directory (parent directory):
#         cd ..
#  iv. Go to the previous directory:
#         cd -

# 2. Print Working Directory — pwd

# The pwd (print working directory) command shows the absolute path of your current directory.
#       pwd